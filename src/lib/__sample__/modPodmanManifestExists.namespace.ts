export namespace modPodmanManifestExists {
  const conf = {
    usages: ["podman manifest exists MANIFEST"],
    usageMatrix: [
      {
        argsStr: ["MANIFEST"],
        args: [
          {
            keyword: "options",
            literal: "options",
            kind: "options",
            isOptional: true,
            isOptionsArg: true,
            isSpread: false,
          },
          {
            keyword: "manifest",
            literal: "MANIFEST",
            kind: "argument",
            isOptionsArg: false,
            isOptional: false,
            isSpread: false,
          },
        ],
        tsOptions: {
          options: { kind: "Options", optional: true },
          manifest: { kind: "string", optional: false },
        },
        tsOptionsSort: ["options", "manifest"],
        tsOptionsStr: "{options?: Options;manifest: string;}",
      },
    ],
    command: "podman manifest exists",
    options: {},
  };

  type Options = {};

  export type optionsArgument = { options?: Options; manifest: string };

  export function parseOptionsArgument(options: optionsArgument): string[] {
    const optionsArguments = Object.entries(options.options ?? {}).map(
      ([key, value]) => {
        return [
          `${Reflect.get(conf.options, key).flag}`,
          ...(value === true ? [] : [`${value}`]),
        ];
      },
    );

    const usageMatrix = conf.usageMatrix[0];

    const parseValues = usageMatrix.tsOptionsSort
      .map((key) => {
        const optionValue = Reflect.get(options, key);
        return Reflect.get(usageMatrix.tsOptions, key).kind === "Options"
          ? optionsArguments.flat()
          : optionValue === undefined
            ? []
            : [optionValue];
      })
      .flat();

    return parseValues;
  }
}
